fn main(){

    // Explicitly define a bool
    let is_bool1:bool = true;
    println!("Explicitly defined bool: {}", is_bool1);

    // Implicitly define a bool

    let is_bool2 = false;
    println!("Implicitly defined bool: {}", is_bool2);

    // Result of an expression

    let is_bool3 = 10 > 3;
    println!("Expression of is_bool3: {}", is_bool3);

    

}