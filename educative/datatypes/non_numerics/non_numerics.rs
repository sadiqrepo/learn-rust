fn main(){

    // Defining a character explicitly
    let char1:char = 'a';
    println!("Character 1: {}",char1);

    // Defining a character implicitly
    let char2 = 'b';
    println!("Character 2: {}", char2);

    // Defining a string explicitly
    let str1:&str = "Rust programming language";
    println!("String 1: {}",str1);

    // Defining a string implicitly
    let str2 = "Beginner's course from educative";
    println!("String 2: {}",str2);


}